    public void fetch()
    {        
         try {
            String val1=date1.getText();
            String val2=date2.getText();
            JTableHeader header = table.getTableHeader();
            header.setForeground(Color.red);
            header.setFont(new Font("Tahome",Font.ITALIC, 12));            
            String q ="select receiving_party as Party,receive_date as 'Receive Date',item_category as 'Item type',item_model as 'Item category',item_quantity as Quantity from item where receive_date between '"+val1+"' and '"+ val2+"' ";             
            pst = conn.prepareStatement(q);
            rs = pst.executeQuery();            
            table.setModel(DbUtils.resultSetToTableModel(rs));             
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Item not Fetched");
        }

    }
