  public static boolean valDOB(String DOB)
    {
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Date BOD = null;
        df.setLenient(false);
        try{
            
            BOD=df.parse(DOB);
            return true;
        }catch(Exception e)
        {
            return false;
        }
    }
